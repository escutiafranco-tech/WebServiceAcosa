-- Crear tablas de proveedores en acosa.fdb

-- Tabla principal de proveedores
CREATE TABLE TBL_PROV_DFISCALES (
  ID VARCHAR(50) NOT NULL PRIMARY KEY,
  CODIGO VARCHAR(50),
  NOMBRE VARCHAR(255),
  RFC VARCHAR(50),
  DIRECCION BLOB SUB_TYPE TEXT,
  ACTIVO SMALLINT,
  FECHA_REGISTRO VARCHAR(50)
);

-- Tabla de servicios
CREATE TABLE TBL_PROV_DSERVICIOS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PROVEEDOR_ID VARCHAR(50) NOT NULL,
  SERVICIO VARCHAR(255),
  DESCRIPCION BLOB SUB_TYPE TEXT,
  CONSTRAINT FK_DSERVICIOS_PROV FOREIGN KEY (PROVEEDOR_ID) 
    REFERENCES TBL_PROV_DFISCALES(ID) ON DELETE CASCADE
);

-- Tabla de sucursales
CREATE TABLE TBL_PROV_DSUCURSALES (
  ID VARCHAR(50) NOT NULL PRIMARY KEY,
  PROVEEDOR_ID VARCHAR(50) NOT NULL,
  TIPO VARCHAR(50),
  NOMBRE VARCHAR(255),
  PAIS VARCHAR(100),
  ESTADO VARCHAR(100),
  MUNICIPIO VARCHAR(100),
  LOCALIDAD VARCHAR(100),
  CALLE VARCHAR(255),
  COLONIA VARCHAR(255),
  CP VARCHAR(20),
  CONSTRAINT FK_DSUCURSALES_PROV FOREIGN KEY (PROVEEDOR_ID) 
    REFERENCES TBL_PROV_DFISCALES(ID) ON DELETE CASCADE
);

-- Tabla de contactos/agenda
CREATE TABLE TBL_PROV_DAGENDA (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PROVEEDOR_ID VARCHAR(50) NOT NULL,
  NOMBRE VARCHAR(255),
  CARGO VARCHAR(100),
  TELEFONO VARCHAR(50),
  EMAIL VARCHAR(255),
  CONSTRAINT FK_DAGENDA_PROV FOREIGN KEY (PROVEEDOR_ID) 
    REFERENCES TBL_PROV_DFISCALES(ID) ON DELETE CASCADE
);

-- Tabla de Productos y Servicios (catálogo general)
CREATE TABLE TBL_PROD_SERVICIOS (
  ID VARCHAR(50) NOT NULL PRIMARY KEY,
  NOMBRE VARCHAR(255) NOT NULL,
  DESCRIPCION BLOB SUB_TYPE TEXT,
  ACTIVO SMALLINT DEFAULT 1,
  FECHA_REGISTRO VARCHAR(50)
);

-- Tabla de relación Proveedor - Productos/Servicios
CREATE TABLE TBL_PROV_PROD_SERVICIOS (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PROVEEDOR_ID VARCHAR(50) NOT NULL,
  PROD_SERVICIO_ID VARCHAR(50) NOT NULL,
  FECHA_ASIGNACION VARCHAR(50),
  CONSTRAINT FK_PROV_PROD_PROV FOREIGN KEY (PROVEEDOR_ID) 
    REFERENCES TBL_PROV_DFISCALES(ID) ON DELETE CASCADE,
  CONSTRAINT FK_PROV_PROD_PROD FOREIGN KEY (PROD_SERVICIO_ID) 
    REFERENCES TBL_PROD_SERVICIOS(ID) ON DELETE CASCADE,
  CONSTRAINT UK_PROV_PROD UNIQUE (PROVEEDOR_ID, PROD_SERVICIO_ID)
);

COMMIT;
